public class zweiArgumenteEinWort {
    public static void main(String[] args) {
        int ganzeZahl = 44;
        double gleitZahl = 0.44;
        String wort = "Decke";

        System.out.println("" + ganzeZahl + " + 5 = " + (ganzeZahl + 5) + ", " + gleitZahl + " / 3 = " + (gleitZahl / 3) + ", Wort: " + wort + ".");
    }
}
